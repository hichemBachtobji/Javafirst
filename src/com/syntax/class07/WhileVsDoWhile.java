package com.syntax.class07;

public class WhileVsDoWhile {

	public static void main(String[] args) {
		
		
		// hello 5 times
		int num=1;
		
		while (num<=5) {
			System.out.println("hello");
			num++;
	}
		System.out.println("---------------WHILE LOOP------------------");
		// HELLO 5 TIMES
		int num1=1;
		
		do {
			System.out.println("hello");
			num1++;
		}while(num1<=5);
		

	}

}
